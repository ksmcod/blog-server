const mongoose = require('mongoose');

const Schema = mongoose.Schema

const blogSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

const blogModel = mongoose.model('Blog',blogSchema);